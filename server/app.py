# ─── /tasks ───────────────────────────────────────────────────────────────────
@app.get("/tasks")
async def list_tasks():
    """
    Return all tasks with descriptions, difficulty ratings, and action schemas.
    Used by agents to self-document available tools and by the hackathon validator.
    """
    tasks_out = []
    for tid, task in TASKS.items():
        tasks_out.append({
            "id": tid,
            "description": task["description"],
            "difficulty": task["difficulty"],
            "max_steps": 20,
            "action_schema": task["action_schema"],
            "subgoals": [
                {"name": name, "weight": weight}
                for name, weight in SUBGOALS[tid]
            ],
            "action_types": [
                "submit_query",
                "query_schema",
                "inspect_data",
                "list_tables",
                "run_test",
            ],
            "action_fields": {
                "action_type": "str — one of the action_types above",
                "sql_query":   "str | None — SQL to execute (submit_query only)",
                "target_table": "str | None — table name (query_schema / inspect_data only)",
            },
        })
    return {"tasks": tasks_out, "count": len(tasks_out)}


# ─── /grader ──────────────────────────────────────────────────────────────────
class GraderRequest(BaseModel):
    episode_id: Optional[str] = None
    task_id: str = "easy"


@app.post("/grader")
async def grade_episode(req: GraderRequest):
    """
    Return the current grader score for the active episode.
    Call this after an episode completes (done=True) to retrieve the final score.
    """
    task_id = req.task_id
    if task_id not in TASKS:
        raise HTTPException(status_code=400, detail=f"Unknown task_id '{task_id}'. Valid: {list(TASKS.keys())}")

    score = env.get_current_score()
    flags = env.get_achieved_flags()
    breakdown = {
        name: {"achieved": flags.get(name, False), "weight": weight}
        for name, weight in SUBGOALS[task_id]
    }

    return {
        "episode_id": req.episode_id or env.state.episode_id,
        "task_id": task_id,
        "score": score,
        "max_score": 1.0,
        "subgoal_breakdown": breakdown,
    }


# ─── /baseline ────────────────────────────────────────────────────────────────
_baseline_lock = asyncio.Lock()
_baseline_cache: Optional[Dict[str, Any]] = None


@app.post("/baseline")
async def run_baseline(force: bool = False):
    """
    Trigger the baseline inference script (inference.py).
    Returns reproducible scores for all three tasks.
    Results are cached so repeated calls during the hackathon validator don't
    spin up multiple OpenAI calls. Pass ?force=true to re-run.
    """
    global _baseline_cache

    if _baseline_cache is not None and not force:
        return _baseline_cache

    async with _baseline_lock:
        if _baseline_cache is not None and not force:
            return _baseline_cache

        script_path = os.path.join(
            os.path.dirname(os.path.dirname(os.path.abspath(__file__))),
            "inference.py",
        )

        if not os.path.exists(script_path):
            raise HTTPException(status_code=500, detail="inference.py not found.")

        try:
            proc = await asyncio.create_subprocess_exec(
                sys.executable, script_path,
                stdout=asyncio.subprocess.PIPE,
                stderr=asyncio.subprocess.PIPE,
            )
            stdout, stderr = await asyncio.wait_for(proc.communicate(), timeout=300)
        except asyncio.TimeoutError:
            raise HTTPException(status_code=504, detail="Baseline script timed out (300s).")
        except Exception as e:
            raise HTTPException(status_code=500, detail=f"Failed to launch baseline script: {e}")

        if proc.returncode != 0:
            raise HTTPException(
                status_code=500,
                detail=f"Baseline script exited {proc.returncode}. stderr: {stderr.decode()[:500]}",
            )

        try:
            result = json.loads(stdout.decode())
        except json.JSONDecodeError:
            raise HTTPException(
                status_code=500,
                detail=f"Baseline script stdout is not valid JSON: {stdout.decode()[:300]}",
            )

        _baseline_cache = result
        return result

